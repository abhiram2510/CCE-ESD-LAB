	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
    DCD 0x10001000
    DCD Reset_Handler
    ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC
	LDR R1,=DST
	MOV R2,#10
	LDR R3, =DST+(N-1)*4
	LDR R4, =DST+(N-1)*4+(SH*4)
LOOP
	LDR R5,[R0],#4
	STR R5,[R1],#4
	SUBS R2,#1
	BNE LOOP
	MOV R2,#10+(N-SH)
LOOOP
	LDR R6,[R3],#-4
	STR R6,[R4],#-4
	SUBS R2,#1
	BNE LOOOP
STOP 
	B STOP
N EQU 10
SH EQU 4
SRC DCD 0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,0x9,0xA
	AREA area,DATA,READWRITE
DST DCD 0
	END