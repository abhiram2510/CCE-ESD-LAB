	;hex to ASCII
	AREA reset,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode , CODE,READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0,=HEX
	LDR R1,=DST
	LDRB R2,[R0]
	AND R3,R2,#0x0F
	CMP R3,#0x09
	BEQ ADD30
	BMI ADD30
	ADD R3 ,#0x07
	B ADD30
ADD30
	ADD R3,#0x30
	STRB R3,[R1]
	LSR R3,R2,#4
	CMP R3,#0x09
	BEQ ADD30II
	BMI ADD30II
	ADD R3,#0x07
	B ADD30II
ADD30II
	ADD R3,#0x30
	STRB R3,[R1,#1]
STOP
	B STOP


HEX DCD 0x00000072
	AREA data, DATA, READWRITE
DST DCD 0
	END