    AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R7,=SRC
	LDR R0,[R7]
	MOV R1,#0x0F
	MOV R2,#0x08 ;counter
	MOV R8,#0x0 ;no of 1's
	AND R3,R0,R1
	B CHECK8
SHIFT
	SUB R2,#1
	CMP R2,#0
	BEQ STOP
	LSR R0,R0,#4
	AND R3,R0,R1
	B CHECK8
CHECK8
	CMP R3,#0x8
	BMI CHECK4
	SUB R3,R3,#0x8
	ADD R8,#1
	B CHECK4
CHECK4
	CMP R3,#0x4
	BMI CHECK2
	SUB R3,R3,#0x4
	ADD R8,#1
	B CHECK2
CHECK2
	CMP R3,#0x2
	BMI CHECK1
	SUB R3,R3,#0x2
	ADD R8,#1
	B CHECK1
CHECK1
	CMP R3,#0x1
	BMI SHIFT
	SUB R3,R3,#0x1
	ADD R8,#1
	B SHIFT
STOP
	B STOP
SRC DCD 0xFFFFFFFF
	AREA mydata, DATA, READWRITE
RES DCD 0
	END